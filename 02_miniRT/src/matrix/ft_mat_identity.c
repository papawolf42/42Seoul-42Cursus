#include "matrix.h"

t_mat44				ft_mat_identity(void)
{
	return (ft_mat_set(ft_vset(1, 0, 0),
						ft_vset(0, 1, 0),
						ft_vset(0, 0, 1),
						ft_vset(0, 0, 0)));
}
