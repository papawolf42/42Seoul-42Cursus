#include "console.h"

void			ft_console_color(t_color color)
{
	printf("%-10s%-3d %-3d %-3d\n", TC_COLOR, (int)(color.x * 255.999),
					(int)(color.y * 255.999), (int)(color.z * 255.999));
}
